#Import Libraries

import scipy as sci
import sklearn as skl
from sklearn.neighbors import KNeighborsClassifier
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
import plotly

#Use Pandas To Read The CSV File

Drink_Groups = pd.read_csv('Drink Calorie Prediction.csv')

#Set Sodium And Calories Columns Equal To A Variable

sodium_list = []
calories_list = []
sodium = Drink_Groups['Sodium']
calories = Drink_Groups['Calories']

for a in sodium:
  sodium_list.append([int(a)])

sodium = sodium_list[1:]

calories = calories.tolist()

for b in calories:
  calories_list.append(int(b))

calories = calories_list[:-1]

#Enter The Sodium Amount In The Brackets To Get The Estimated Calories For The 20 Fluid Ounce Drink

neighbors = KNeighborsClassifier(n_neighbors = 4)
neighbors.fit(sodium, calories)
print(neighbors.predict([[100]]))

plt.scatter(sodium, calories)